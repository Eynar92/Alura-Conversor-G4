    private void currencyOneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_currencyOneKeyTyped
        char inputChar = evt.getKeyChar();
        int caretPosition = currencyOne.getCaretPosition();

        try {
            if (Character.isDigit(inputChar) || inputChar == '.') {
                if (inputChar == '.' && cantidad.contains(".")) {
                    evt.consume();
                    return;
                }

                String selectedText = currencyOne.getSelectedText();
                if (selectedText != null) {
                    int selectionStart = currencyOne.getSelectionStart();
                    int selectionEnd = currencyOne.getSelectionEnd();
                    cantidad = cantidad.substring(0, selectionStart) + cantidad.substring(selectionEnd);
                    caretPosition = selectionStart;
                }
                StringBuilder builder = new StringBuilder(cantidad);
                if (!cantidad.isEmpty() && caretPosition < cantidad.length()) {
                    builder.setCharAt(caretPosition, inputChar);
                } else {
                    builder.append(inputChar);
                }
                cantidad = builder.toString();
                //cantidad += cantidad.substring(0, caretPosition) + inputChar + cantidad.substring(caretPosition);
                obtenerDinero(cantidad);
            } else if (inputChar == evt.VK_BACK_SPACE) {
                eliminarUltimoDigito();
                obtenerDinero(cantidad);
            } else {
                JOptionPane.showMessageDialog(this, "Solo se permiten números", "Error", JOptionPane.ERROR_MESSAGE);
                evt.consume();
            }

            System.out.println(cantidad);
            System.out.println(dinero);
        } catch (NumberFormatException e) {
            throw new NumberFormatException();
        }
    }//GEN-LAST:event_currencyOneKeyTyped

