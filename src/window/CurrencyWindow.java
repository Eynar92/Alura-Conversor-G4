    private void obtenerDinero(String cantidad) {
        if (cantidad.isEmpty()) {
            dinero = 0.0;
        } else {
            dinero = Double.valueOf(cantidad);
        }
        cambioDivisas();
        dinero = dinero * cambio;
        currencyTwo.setText(String.format("%.2f", dinero));
    }

